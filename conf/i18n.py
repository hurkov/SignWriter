def t(lang: str, key: str, **kwargs) -> str:
    """Translate key for language with fallback to English."""
    data = TRANSLATIONS.get(lang, TRANSLATIONS["en"])
    text = data.get(key, TRANSLATIONS["en"].get(key, key))
    if kwargs:
        try:
            return text.format(**kwargs)
        except Exception:
            return text
    return text
